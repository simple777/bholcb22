                        <p>В 1962 году районной библиотеке присвоено звание «Библиотека отличной работы», Знаком Министерства
                            культуры РСФСР «За отличную работу», награждена сотрудник районной библиотеки Нонна Ивановна Щербакова. </p>

                        <p>В 1963 г. руководство библиотекой переходит к Вере Афанасьевне Девятых. Идёт активное комплектование
