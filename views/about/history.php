                        <p>В послевоенный период библиотека возглавила работу среди широких масс населения по преодолению
                            трудностей оставленных войной. Особое внимание уделялось обслуживанию участников и инвалидов Великой Отечественной войны.</p>

                        <p>В 1946 году заведующей назначена <a href="#">Тамара Николаевна Зубкова</a>.</p>

