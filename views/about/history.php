                        <p>Белохолуницкая народная библиотека открылась в Белохолуницком посёлке 23 мая 1896 года по
                            приговору Главнохолуницкого волостного схода. Открыта она была на средства управляющего
                            заводами А. А. фон Зигеля (в сумме 250 рублей). Его и утвердили пожизненным почетным
                            попечителем библиотеки. Библиотека помещалась в Доме местного благотворительного общества.
                            В 1902 году в библиотеке было 458 читателей, фонд насчитывал 1172 экземпляра. В 1912-1913
                            годах для библиотеки были отпущены средства от губернского земства по 50 рублей в год.</p>

                        <p>После Октябрьской социалистической революции Белохолуницкая земская библиотека была переименована
                            в Белохолуницкую районную библиотеку и переведена в новое здание. Весь книжный фонд в 1920 г.
                            составлял 2924 экземпляра. Поступило за 1920 г. — 1752 экз., выбыло — 53. Состояло на
                            учете на 1 01. 1921 г. — 4623 экз. Читателей в библиотеке было 530 человек, из них мужчин — 229,
                            женщин — 301, выдано книг – 10183 экземпляра. Выписывали газеты: «Вятская правда»,
                            «Правда Москвы», «Правда Петрограда», «Беднота», «Уральский рабочий», «Известия ЦИКа»,
                            журналы: «Ученый», «Нива». Первый заведующий - <a href="#">Борис Николаевич Борисов</a>.</p>

                        <p>В 1923 году заведующей библиотекой назначена <a href="#">Анна Николаевна Куварзина</a>.
                            Для привлечения читателей организовывались передвижки. В 1925 году их работало при библиотеке 10.</p>

                        <p>С 1938 по 1941 год заведующей библиотекой работает <a href="#">Антонина Михайловна Смирнова</a>.</p>
                        <p>В годы Великой Отечественной войны сотрудники библиотеки оказывали помощь эвакуированному
