					<h2>Штаб победы</h2>
					
					<div class="project-victory-col1"> 
						<p>1 октября в конференц-зале Кировской областной научной библиотеки им. А. И. Герцена состоялась церемония награждения победителей грантового конкурса «Память сердца», организованного по инициативе депутата Государственной Думы РФ Р. А. Азимова.</p>
						
						<p>Цель конкурса - поддержка инициатив, направленных на сохранение памяти о Победе в Великой Отечественной войне. Сертификаты победителей были вручены 
						43 победителям из 22-х муниципальных районов Кировской области и города Кирова. Сертификат победителя в номинации "Голоса Победы" получили представители команды проекта "Штаб Победы" Белохолуницкой центральной библиотеки.</p>

						<p>Проект направлен на развитие нравственно-патриотического воспитания подрастающего поколения, любви к Родине, преданности, ответственности и гордости за неё. Основное назначение проекта – создание условий для освоения учащимися социально-значимого опыта в различных видах деятельности по данному направлению. В нем представлен цикл мероприятий, посвященных событиям, в которых в полной мере проявился героизм и патриотизм русских людей в годы Великой Отечественной войны:</p>

							<ul>
								<li><p>цикл мероприятий «Славные страницы великих битв»</p></li>
								<li><p>конкурс поисково-исследовательских работ «Память сквозь поколения» (на основе воспоминаний участников войны, тружеников тыла, детей военной поры);</p></li>
								<li><p>цикл часов документального кино «Летопись огненных лет»;</p></li>
								<li><p>встречи тружеников тыла, детей военной поры и их родственниками с учащимися школ города и др.</p></li>
							</ul>

						<p>Уникальность проекта заключалась в создании военного штаба,  обстановка которого будет способствовать погружению в атмосферу тех лет. Для этого была использована военная атрибутика: маскировочная сетка, военная форма, флаг СССР, портреты маршалов войны, планшет офицера, керосиновая лампа и т.п. Знакомство с основными сражениями Великой Отечественной войны сопровождалось просмотром фрагментов из документальных фильмов и схематичным изображением битв на масштабной карте. Также школьники моли применить новые знания в настольной игре-стратегии цикла «Великая Отечественная». </p>
						
						<p>Это повысило интерес учащихся к циклу «Славные страницы великих битв», другим мероприятиям проекта и изучению истории войны в целом.</p>

						<p>В ходе рабочей поездки 29 ноября центральную и детскую библиотеки г. Белая Холуница посетил депутат Государственной Думы Рахим Азизбоевич Азимов. Для него прошла презентация проекта «Штаб Победы». Рахим Азизбоевич посетил комнату, которая оформлялась под фронтовой штаб, узнал, на какой стадии находится процесс реализации проекта.</p> 

						<p>Реализация проекта «Штаб Победы» началась в ноябре 2019 года согласно плану. На втором этаже библиотеки выделено помещение, в котором был сделан косметический ремонт и создан Штаб.</p>

						<p>Проведение мероприятий для школьников старшего и среднего звена началось с января 2020 года. В рамках цикла «Славные страницы великих битв» дети обобщили и расширили свои знания о начале войны, о битве под Москвой, о Сталинградской битве и битве под Прохоровкой. Не обошли вниманием и тяжелое время блокадного Ленинграда. Каждое мероприятие сопровождалось электронной презентацией и просмотром видеохроники военных лет. Всего проведено 10 занятий для учащихся 5, 8, 9 и 11 классов КОГОБУ СШ с УИОП г. Белой Холуницы и учащихся 8 класса КОГОБУ ШИ ОВЗ №1 г. Белая Холуница. Посещения составили 208.</p>
								
						<p>В рамках проекта 3 февраля стартовал районный конкурс поисково-исследовательских работ «Память сквозь поколения: Дороги фронтовые», участникам которого необходимо проследить перемещения воина-родственника от места призыва до места окончания его участия в событиях военного периода. На конкурс поступило 8 работ, авторам которых вручены Дипломы и Благодарственные письма.</p>
							
						<p>Для младших школьников были организованы экскурсии в «Штаб Победы», где они смогли увидеть и потрогать предметы и вещи военной поры: флаг СССР, офицерский планшет, кобуру, пулемётную ленту и другие. Интересны были и настольные игры серии «Великая Отечественная» с фигурками солдат, оружия и военной техники. Также экскурсия была проведена для участников конкурса «Живая классика-2020». Эти мероприятия прошли внепланово, по инициативе сотрудников библиотеки и педагогов школ города.</p>

						<p>В дальнейшем планируется продолжить работу передвижного «Штаба Победы» в филиалах Муниципального бюджетного учреждения культуры «Белохолуницкая центральная библиотека» с учащимися школ Белохолуницкого района.</p>

