                            развешивание искусственных гнезд. Информация об акции размещается на стенде библиотеки,
                            предоставляется в детские сады.</p>

                        <p>«Неделя детской книги», открытием которой традиционно стало выступление перед ребятами
                            со своеобразным отчётом – презентацией «Нам есть что вспомнить!».</p>

                        <p>«Библионочь», во время которой посетителями библиотеки ежегодно становятся более 200 детей.</p>

                        <p>В холле библиотеки стали традиционными выставки работ ребят, занимающихся в кружках
                            декоративно – прикладного творчества городского дома культуры. Руководитель –
                            Шмакова Елена Викторовна. Выставки пользуются большой популярностью у детей и взрослых.</p>

                        <p>На фото – работы в технике аппликация пайетками.</p>

                        <p>Рисунки детей, выполненные на конкурс «Пасхальная радость», целью которого является духовное
                            просвещение, нравственное и патриотическое воспитание подрастающего поколения.</p>

                        <p>Беседа для воспитанников детского сада у выставки работ, представленных на городской
                            конкурс «Безопасная ёлка», организованного по инициативе ВДПО.</p>

                        <p>Большое внимание библиотека уделяет работе с книгой летом, которая ведётся по двум
                            традиционным направлениям: работа с летними лагерями отдыха; работа с летними списками
                            литературы. По просьбе сотрудников библиотеки педагогами в летние списки для будущих
                            второклассников были включены произведения местных писателей-земляков, в первую очередь
                            Н. Марихина, а также Л. Дьяконова, В. Ситникова, Н. Русиновой, В. Морозова, М. Чиркова.
