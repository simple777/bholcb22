                        <p>Библиотека обслуживает детское население города, руководителей детского чтения, является
                            методическим центром для библиотек района по работе с детьми и подростками. Активно
                            сотрудничает с детскими садами районного центра, государственной и муниципальной школами,
                            школой – интернатом IV вида (школа слабовидящих), специальной (коррекционной) школой –
                            интернатом VII вида (детский дом), спортивной школой, Детской школой искусств,
                            Домом детского творчества.</p>

                        <p>Фонд библиотеки насчитывает свыше 19 тыс. экземпляров печатных и электронных документов.
                            Ежегодно услугами библиотеки пользуется свыше 2 тыс. пользователей, им выдается свыше
                            39 тыс. экземпляров книг и периодических изданий.</p>

                        <p>Основным направлением деятельности библиотеки является работа с литературным наследием
                            Николая Ильича Марихина в рамках экологического, нравственного и историко-патриотического
                            воспитания подрастающего поколения.</p>

                        <p>Основные задачи, стоящие перед библиотекой: формирование интереса к книге и чтению у
                            дошкольников; помощь учащимся в освоении школьной программы; организация летнего чтения
                            детей; наиболее полное удовлетворение читательских запросов школьников и руководителей
                            детского чтения.</p>

                        <p>Библиотека работает по программе «Вырастим читателя». Ежегодно в рамках программы
                            проводится свыше 130 мероприятий, которые посещает более 3000 человек.</p>

                        <p>Для детей проводятся сказочные путешествия, игровые карусели, творческие конкурсы, часы
                            веселых затей, электронные викторины, экскурсии по библиотеке. Библиотечные уроки «Как
                            книжка на свет появилась» и «Вселенная от А до Я» (по страницам справочных изданий)
                            знакомят ребят с миром книги. Традиционно для детей проводятся обзоры журнальных
                            новинок читального зала «Журнальный калейдоскоп».</p>

                        <p>Работники стараются использовать нетрадиционные формы работы литературные квесты,
                            интерактивные игры с демонстрацией электронных викторин.</p>

                        <p>Во время осенних каникул для учащихся 2-х классов организован «Осенний литературный марафон».
                            По спискам литературы, которые включали внепрограммные произведения В. Драгунского,
                            Ю. Сотника, В. Голявкина, Ю. Дмитриева, М. Зощенко, Е. Шварца, В. Бианки, дети должны
                            были во время недели прочитать как можно больше литературы, а после каникул по этим
                            произведениям были проведены проверочные викторины. В результате всю неделю учащиеся
                            2-х классов были постоянными читателями библиотеки. Всего поучаствовало 48 второклассников,
                            они посетили библиотеку 113 раз, книговыдача составила 241 экземпляр.</p>

                        <p>Учащиеся 4-х классов совершили экскурсию по выставке «Путешествие в Книгоград», на которой
                            представлены работы участников районного литературного конкурса «Воздух детства и отчего
                            дома» с рисунками своих любимых книг.</p>


                        <p>Ежегодно библиотека проводит для первоклассников праздник «Прощание с Азбукой».</p>

                        <p>3 апреля 2015 года в библиотеке состоялось открытие комнаты писателя, журналиста, краеведа,
                            Николая Ильича Марихина, чьё имя носит библиотека.</p>

                        <p>Первыми гостями комнаты Николая Ильича Марихина стали кировские писатели Е. Наумова,
                            В. Пономарёв, В. Ситников, Г. Кустенко, В. Морозов, М. Чирков, А. Купарев Библиотека
                            проводит активную массовую работу со школами и детскими садами по воспитанию юных читателей,
                            пропагандирует книгу и чтение среди родителей.</p>

                        <p>Принимает активное участие в различных акциях: «Покормите птиц»: ежегодно стартует 12 ноября
